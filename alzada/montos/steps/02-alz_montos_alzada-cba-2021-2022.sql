   OR (C_Nro_Expediente LIKE 'ARIT-PTS-%/2021%' OR C_Nro_Expediente LIKE 'ARIT-PTS-%/2022%')
   OR (C_Nro_Expediente LIKE 'ARIT-TJA-%/2021%' OR C_Nro_Expediente LIKE 'ARIT-TJA-%/2022%')
ORDER BY ra.C_Nro_Expediente;
