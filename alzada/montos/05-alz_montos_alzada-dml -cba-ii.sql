                                                                           WHERE g.C_Nro_Expediente = ra.C_Nro_Expediente
                                                                       ) as temp
                                                              ) = 'GOB. DEPARTAMENTALES' THEN 76
                      END
                  ELSE ra.N_Codigo_Impuesto END) AS id_tipo_impuesto,
