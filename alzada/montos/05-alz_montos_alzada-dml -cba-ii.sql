                                                         WHEN (
                                                                  SELECT DISTINCT temp.C_Super_Adm_Tri
                                                                  FROM (
                                                                           SELECT g.C_Super_Adm_Tri
                                                                           FROM (SELECT TAT.C_Super_Adm_Tri, m.C_Nro_Expediente
